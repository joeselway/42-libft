#include "libft.h"

void	*memset(void *b, int c, size_t len)
{
	size_t			i;
	unsigned char	*p;

	p = b;
	i = 0;
	while (i < len)
	{
		*p = (unsigned char)c;
		i++;
	}
	return (b);
}
